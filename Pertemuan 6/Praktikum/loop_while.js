// Deklarasi variabel
var bilangan = 0

// Perulangan while jika bilagan tidak sama dengan 4,
// maka akan mengulang sebanyak 4 kali secara ascending
while(bilangan !=4){
    bilangan ++
    console.log('angka\t: ' + bilangan)
}

console.log('\n')

// Akan mengulang secara descending
do{
    bilangan--
    console.log('angka\t: ' + bilangan)
}while(bilangan !=0)